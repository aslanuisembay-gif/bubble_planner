/// Текущий язык (синхронизируется с [AppState.languageCode]).
String currentLanguageCode = 'en';

/// Получить перевод по ключу.
String tr(String key, {String? lang}) {
  final code = lang ?? currentLanguageCode;
  if (code == 'kk') {
    final kk = _kkUiOverrides[key];
    if (kk != null) return kk;
    final ru = _translations['ru'];
    if (ru != null && ru[key] != null) return ru[key]!;
  }
  final langMap = _translations[code] ?? _translations['en']!;
  return langMap[key] ?? _translations['en']![key] ?? key;
}

/// Подстановка `{n}` и других плейсхолдеров в строке перевода.
String trFill(String key, Map<String, String> params, {String? lang}) {
  var s = tr(key, lang: lang);
  params.forEach((k, v) => s = s.replaceAll('{$k}', v));
  return s;
}
